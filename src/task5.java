public class task5 {
    public static void main(String[] args) {
        var frog = 3.5;

        System.out.println("Исходное значение: " + frog);

        frog *= 10; // Увеличиваем в 10 раз
        System.out.println("После умножения на 10: " + frog);

        frog /= 3.5; // Делим на 3.5
        System.out.println("После деления на 3.5: " + frog);

        frog += 4; // Добавляем 4
        System.out.println("После добавления 4: " + frog);
    }
}
